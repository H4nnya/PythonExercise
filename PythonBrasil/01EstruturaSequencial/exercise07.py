""""
Exercício: 07
Objetivo: Faça um Programa que calcule a área de um quadrado,
em seguida mostre o dobro desta área para o usuário.
"""

b = float(input('Digite a base(cm): '))
h = float(input('Digite a altura(cm): '))
area = b * h
print(f'A área do seu quadrado é {area}cm² e o dobro é {area*2}cm².')
